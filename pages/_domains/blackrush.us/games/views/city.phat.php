            The game is a sequel to City at World's End by Edmond Hamilton.
            <br>
            <br>
            The purpose of the game is to explore the game world, discover the real truth behind the events of the first book,
            and avert a planetary cataclysmic disaster.
            <br>
            <br>
            It picks up around a year or two after the events of the 1950 novel. The Cities of Middletown and New Middletown
            are located in what was the midwestern US, however it is not generally known what the current year actually is.
            From the perspective of the native citizens it is somewhere between "millions" and "one billion" years from
            their original time.
